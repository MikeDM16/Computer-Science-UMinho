    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        Prestacao p = this.listaPrestacao.get(this.jList1.getSelectedIndex());
        try {
            this.s.pagarPrestacao(p);
        }
        catch (SemSaldoException e) {
            JOptionPane.showMessageDialog(this, "Não tem saldo suficiente", "Atenção", ERROR_MESSAGE);
            return;
        }
        this.dispose();
    }//GEN-LAST:event_jButton1MouseClicked

