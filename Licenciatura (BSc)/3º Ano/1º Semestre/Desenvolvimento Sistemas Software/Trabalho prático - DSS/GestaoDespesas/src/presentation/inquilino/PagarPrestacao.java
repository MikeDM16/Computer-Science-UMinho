    private void jList1ValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_jList1ValueChanged
        Prestacao p = this.listaPrestacao.get(this.jList1.getSelectedIndex());
        Despesa d = this.s.getDespesa(p);
        this.jTextField1.setText("Despesa " + d.getRef());
        this.jTextField2.setText(Double.toString(p.getValor()));
        this.jTextField4.setText(Integer.toString(p.getNrSeq()));
        this.jTextField5.setText(Double.toString(this.s.getValorAPagarAtualUser(d)));
    }//GEN-LAST:event_jList1ValueChanged

