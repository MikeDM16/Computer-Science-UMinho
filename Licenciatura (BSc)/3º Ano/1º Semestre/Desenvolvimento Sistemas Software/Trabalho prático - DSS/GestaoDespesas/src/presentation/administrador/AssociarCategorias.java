    private void jComboBox1ItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_jComboBox1ItemStateChanged
        this.percentagensAtuais.clear();
        this.percentagensAtuais.putAll(this.s.getPercentagensCategorias(this.jComboBox1.getSelectedItem().toString()));
        preencherTabela();
    }//GEN-LAST:event_jComboBox1ItemStateChanged

    private void preencherTabela() {
        DefaultTableModel modelT = (DefaultTableModel) jTable1.getModel();
        modelT.setRowCount(0);
        for (Map.Entry<String, Double> e : this.percentagensAtuais.entrySet()) {
            Vector v = new Vector(2);
            v.add(0, e.getKey());
            v.add(1, e.getValue());
            modelT.addRow(v);
        }
    }

    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        String categoria = this.jList2.getSelectedValue();
        Double percentagem = (Double) this.jSpinner2.getValue();
        DefaultTableModel modelT = (DefaultTableModel) jTable1.getModel();
        int j;
        for (j = 0; j < modelT.getRowCount(); j++) {
            if (modelT.getValueAt(j, 0).toString().equals(categoria)) {
                break;
            };
        }
        if (j < modelT.getRowCount()) {
            modelT.setValueAt(percentagem, j, 1);
        } else {
            Vector v = new Vector<>(2);
            v.add(0, categoria);
            v.add(1, percentagem);
            modelT.addRow(v);
        }
    }//GEN-LAST:event_jButton1MouseClicked

    private void jButton2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MouseClicked
        DefaultTableModel model = (DefaultTableModel) this.jTable1.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            this.s.associarCategoriaInquilino(this.jComboBox1.getSelectedItem().toString(),
                    model.getValueAt(i, 0).toString(), (Double) model.getValueAt(i, 1));
        }
        this.dispose();
    }//GEN-LAST:event_jButton2MouseClicked

