    private void remover_action(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_remover_action
        // Add your handling code here:
        String num = this.numero.getText();
        if (!num.equals("")) {
            try {
                this.turma.delAluno(num);
                this.limpar_action(evt);
            }
            catch (TurmaException a) {
                javax.swing.JOptionPane.showMessageDialog(this, "Aluno "+num+" n√£o existe!\nPor favor indique um n√∫mero de aluno v√°lido.", "Aluno inexistente", 0);
            }
        }  else
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor indique um n√∫mero de aluno.", "Dados incompletos", 0);
    }//GEN-LAST:event_remover_action

    private void consultar_action(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_consultar_action
        // Add your handling code here:
        String num = this.numero.getText();
        if (!num.equals("")) {
            try {
                Aluno a=this.turma.getAluno(num);
                
                this.nome.setText(a.getNome());
                this.nota_teorica.setSelectedItem(""+a.getNotaT());
                this.nota_pratica.setValue(a.getNotaP());
                this.media.setValue(a.getMedia());
            }
            catch (TurmaException a) {
                javax.swing.JOptionPane.showMessageDialog(this, "Aluno "+num+" n√£o existe!\nPor favor indique um n√∫mero de aluno v√°lido.", "Aluno inexistente", 0);
            }
        }  else
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor indique um n√∫mero de aluno.", "Dados incompletos", 0);
    }//GEN-LAST:event_consultar_action

    private boolean validaDados() {
        boolean vazio = this.numero.getText().equals("") || this.nome.getText().equals("");
        
        if (vazio)
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor preencha todos os dados.", "Dados incompletos", 0);
        
        return !vazio;
    }
    
    private void adicionar_action(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionar_action
        // Add your handling code here:
        if (this.validaDados()) {
            String num = this.numero.getText();
            String nome = this.nome.getText();
            int notaT = Integer.parseInt((String)this.nota_teorica.getSelectedItem());
            int notaP = this.nota_pratica.getValue();
            
            this.turma.addAluno(new Aluno(num, nome, notaT, notaP));
        }        
    }//GEN-LAST:event_adicionar_action

    private void limpar_action(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_limpar_action
        // Add your handling code here:
        this.nome.setText("");
        this.numero.setText("");
        this.nota_teorica.setSelectedIndex(9);
        this.nota_pratica.setValue(10);
        this.media.setValue(0);
    }//GEN-LAST:event_limpar_action

